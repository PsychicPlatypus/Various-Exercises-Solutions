# For a given list of string
# Count how many strings:
# Have 3 vowels in a row, without 2 of the same vowels one after another (aoa is fine, aao is not fine)
# Are larger than 7 or have more than 2 numbers in them
# If either is not true do not count the string

# Example:
"""
ajkx3
kexpeoe
iii23
2eia0
"""

# Output: 2

# Solution:


def solution(strs: list) -> int:
    count = 0
    for s in strs:
        if len(s) > 7 or len([c for c in s if c.isdigit()]) > 2:
            continue
        if any(
            [
                s[i : i + 3]
                for i in range(len(s) - 2)
                if s[i] in "aeiou"
                and s[i + 1] in "aeiou"
                and s[i + 2] in "aeiou"
                and s[i] != s[i + 1]
                and s[i + 1] != s[i + 2]
                and s[i] != s[i + 2]
            ]
        ):
            continue
        count += 1
    return count


# Test:
print(solution(["ajkx3", "kexpeoe", "iii23", "2eia0"]) == 2)  # False

