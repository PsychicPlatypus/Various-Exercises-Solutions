"""
Story

The Mathia is still coming! Good thing the Academic fifteen minutes seems to be multiplied by each participant. You still have some time to learn how to bake. Having found all the lovely recipes, you need to check that all the instructions make as much sense as possible in this cafeteria run by seemingly mad people.
The manager told you sternly that the previous apprentice is still calculating the last digit of pi after adding salt to the coffee, but not pepper! Rules for the perfect coffee house article are as follows:

• If you add salt, you must add pepper.
• If you knead it, you must let it rest.
• If you add flour, it needs milk too!
• If it has been heated, it needs to be cooled. It is rude to burn people's noses!


And, of course, as you ought to know, you can't let anything rest before it has been kneaded. You can't have it cooled off unless it has been heated first. The same obviously applies to salt/pepper and flour/milk. You also cannot leave an action unfinished, if you finish a previously started action. For example, kneading -> flour -> resting -> milk would not be a proper set of actions as the flour-milk combo should be finished before finishing the kneading-resting combo.

The instructions once again come in a shorthand notation, although this time it is simply the first letter of each rule:

Salt - Pepper: s - p
Kneading - Resting: k - r
Flour - Milk: f - m
Heat - Cool: h - c

You must find the number of correctly structured instructions and count them!
Input Example

skfhcmrp
sspp
fhcksprm
fhksprm
skhfcmrp
Logic Example

First set of actions: valid
Second set of actions: valid
Third set of actions: valid
Fourth set of actions: Invalid, as h is never counteracted with c
Fifth set of actions: Invalid, as h is finished with c before the started f is finished with m.
"""


def solution(doughs: list) -> int:
    """Returns the number of valid instructions in the given string.

    :param s: the string of instructions
    :return: the number of valid instructions
    """
    matching = {
        "s": "p",
        "k": "r",
        "f": "m",
        "h": "c",
    }

    valid = 0
    for dough in doughs:
        stack = []
        for action in dough:
            if action in matching:
                stack.append(action)
            else:
                if not stack or action != matching[stack.pop()]:
                    break
        else:
            if not stack:
                valid += 1
    return valid


